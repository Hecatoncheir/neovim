local make_header = function()
    local week = week_ascii_text_ru()
    local daysoftheweek = { "Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday" }
    local day = daysoftheweek[os.date("*t").wday]
    local tbl = week[day]
    table.insert(tbl, os.date("%Y-%m-%d %H:%M:%S ") .. "")
    return tbl
end

return {
    {
        'nvimdev/dashboard-nvim',
        event = 'VimEnter',
        config = function()
            require('dashboard').setup({
                theme = 'hyper',
                config = {
                    header = make_header(),
                    shortcut = {
                        { desc = 'Show files',    group = 'Files', key = 'leader', action = '<cmd>Oil<Cr>' },
                        { desc = 'Find in files', group = 'Files', key = 'leader', action = '<cmd>Oil<Cr>' },
                    },
                    packages = { enable = false },
                    project = { enable = true, limit = 8 },
                    mru = { enable = true, limit = 8 },
                    footer = {},
                },
            })
        end
    },
}
